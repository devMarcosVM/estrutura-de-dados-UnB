#include <stdio.h>
int main(){
// Exemplos de declaração e inicialização dos tipos básicos
    char caractere = 'A';
    unsigned char uchar = 200;
    int inteiro = 42;
    unsigned int uinteiro = 1000;
    float pontoFlutuante = 3.14;
    double duplo = 12345.6789;
// Exemplos de impressão dos valores
    printf("Char: %c\n", caractere);
    printf("Unsigned Char: %u\n", uchar);
    printf("Int: %d\n", inteiro);
    printf("Unsigned Int: %u\n", uchar);
    printf("Float: %f\n", pontoFlutuante);
    printf("Double: %f\n", duplo);
    return 0;
}